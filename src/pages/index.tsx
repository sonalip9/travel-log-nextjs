import Head from 'next/head';

import { Button } from '@components/Button';
import { Container } from '@components/Container';
import { Text } from '@components/Text';
import { useJournal, useLoginRedirect } from '@hooks';
import { AddCircleOutline } from '@icons';
import { EditJournalModal, Journals } from '@templates/Journals';
import { NavBar } from '@templates/NavBar';

export default function Home() {
  const { data } = useLoginRedirect();

  const { closeModal, journals, modalProps, onDeleteJournal, openModal, pageLoading, visible } =
    useJournal();

  if (!data || pageLoading) return null;

  return (
    <>
      <Head>
        <title>Your Travel Journals</title>
        <meta content="Generated by create next app" name="description" />
        <meta content="width=device-width, initial-scale=1" name="viewport" />
        <link href="/favicon.ico" rel="icon" />
      </Head>
      <main>
        <Container className="min-h-full min-w-full p-xxl bg-background">
          <Container row alignContent="flex-start" className=" mb-xl min-w-full">
            <Text className="display-large m-md flex-1">Journals</Text>
            <NavBar />
          </Container>

          <Container
            row
            className="h-auto min-w-full grid gap-3xl gap-y-3xl grid-cols-[repeat(auto-fit,minmax(406px,1fr))]"
          >
            {journals?.map((journal) => (
              <Journals
                key={journal.journalId}
                journal={journal}
                onDelete={() => onDeleteJournal(journal.journalId)}
                onUpdate={() => openModal(false, journal)}
              />
            ))}
          </Container>

          <Button
            className="fixed bottom-xl right-xl"
            color="primary"
            endContent={<AddCircleOutline />}
            size="lg"
            variant="solid"
            onPress={() => openModal(true)}
          >
            Journal
          </Button>

          {modalProps && (
            <EditJournalModal visible={visible} onCancel={closeModal} {...modalProps} />
          )}
        </Container>
      </main>
    </>
  );
}
