import Head from 'next/head';
import { useSession } from 'next-auth/react';
import { useEffect, useState } from 'react';

import { Container } from '@components/Container';
import { Text } from '@components/Text';
import { UserJournal } from '@defs/journals';
import { useLoginRedirect } from '@hooks';
import { getAllJournals } from '@service/api';
import { Journals } from '@templates/Journals';

export default function Home() {
  useLoginRedirect();
  const { data } = useSession();

  const [journals, setJournals] = useState<UserJournal[]>();

  const [pageLoading, setPageLoading] = useState(true);

  useEffect(() => {
    setPageLoading(true);
    getAllJournals()
      .then((res) => setJournals(res.data.userJournals))
      .catch(console.error)
      .finally(() => setPageLoading(false));
  }, [data]);

  if (!data || pageLoading) return null;

  return (
    <>
      <Head>
        <title>Your Travel Journals</title>
        <meta content="Generated by create next app" name="description" />
        <meta content="width=device-width, initial-scale=1" name="viewport" />
        <link href="/favicon.ico" rel="icon" />
      </Head>
      <main>
        <Container fluid css={{ p: '$xl' }}>
          <Text css={{ m: '$xl' }} type="displayLarge">
            Journals
          </Text>
          <Container
            alignCenter
            fluid
            row
            css={{
              height: '100vh',
              minWidth: '100vw',
              gridTemplateColumns: 'repeat(auto-fit, minmax(406px, 1fr))',
              gridGap: '$xl',
            }}
            display="grid"
            justify="flex-start"
          >
            {journals?.map((journal) => (
              <Journals key={journal.journalId} journal={journal} />
            ))}
          </Container>
        </Container>
      </main>
    </>
  );
}
